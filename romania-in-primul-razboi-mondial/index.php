  					<h4><b>Operația ofensivă în Transilvania</b></h4>
            <h5>Operația ofensivă strategică în Transilvania s-a desfășurat între 27 august și 26 septembrie 1916.

Pentru desfășurarea operației, Marele Cartier General român destinase un număr de 3 armate (1, 2 și de Nord) cu un efectiv total de circa 420.000 de militari, reprezentând aproape 80% din efectivele Armatei de Operații. Obiectivul acestora era de „a înainta în Transilvania și Banat, cu scopul de a se concentra în vederea unei bătălii generale în zona Ciucea (spre nord) și Caransebeș (spre sud)”.</h5>
            <h4><b>Operația de apărare pe frontul de sud</b></h4>
            <h5>Planul de campanie prevedea ca pe frontul de sud Armata României să ducă o operație defensivă strategică. Inițial forțele Armatei 3 urmau să asigure acoperirea frontierei sudice de la Calafat la Marea Neagră urmând ca ulterior, după sosirea Corpului 47 Armată rus să desfășoare o operație ofensivă limitată în vederea scurtării liniei frontului pe linia Rusciuc - Șumla - Varna.Obiectivul acestor două operații era acela de a asigura libertatea de acțiune a forțelor principale ale Armatei României care operau în Transilvania.</h5>
            <h4><b>Bătălia de la Turtucaia</b></h4>
            <h5>Bătălia de la Turtucaia s-a desfășurat între 1-6 septembrie 1916. Pe 2 septembrie, forțele bulgaro-germane aflate sub comanda generalului Mackensen au început să atace pozițiile Armatei României din zona fortificată Turtucaia. </h5>
            <h4><b>Bătălia de la Sibiu</b></h4>
            <h5>Bătălia de la Sibiu s-a desfășurat între 26-29 septembrie 1916, în ea fiind angajate forțele Corpului 1 Armată român (Diviziile 13 și 23 infanterie și Brigada 1 cavalerie) și forțele Armatei 9 germane (Corpul 39 rezervă și Corpul Alpin). Corpul 1 Armată român ocupa un front de 55 de km la sud - sud-est de Sibiu, pe aliniamentul Racovița-Avrig-Săcădat-Cașolț-Gura Râului, cu flancurile sprijinite pe Munții Făgăraș la est și Munții Cibin la vest.</h5>
            <h4><b>Bătălia de la Brașov</b></h4>
            <h5>După victoria de la Sibiu, comandamentul Puterilor Centrale a declanșat contraofensiva împotriva dispozitivului Armatei 2 române care s-a retras prin luptă pe aliniamente succesive, pierzând tot teritoriu dintre Mureș și Olt cucerit în prima fază a operațiilor. Faza finală a acestei succesiuni de retrageri a fost bătălia de la Brașov, desfășurată între 7 și 8 octombrie 1916.</h5>
            <h4><b>Bătălia de pe Valea Prahovei</b></h4>
            <h5>ătălia pentru apărarea defileului Prahovei s-a desfășurat între 9-25 octombrie 1916 și a avut ca rezultat zădărnicirea încercării Armatei 9 germane de forțare a Munților Carpați pe „cel mai scurt drum spre București”. </h5>
            <h4><b>Bătălia din zona Bran-Câmpulung</b></h4>
            <h5>ătălia din zona Bran-Câmpulung s-a desfășurat între 8-23 octombrie 1916 și a avut ca rezultat anihilarea încercării Armatei 9 germane de forțare a Munților Carpați prin Culoarul Rucăr-Bran. </h5>
            <h4><b>Bătălia pe Valea Oltului</b></h4>
            <h5>Bătălia pentru apărarea defileului Oltului s-a desfășurat între 16 octombrie-26 noiembrie 1916 și a avut ca rezultat blocarea încercării Armatei 9 germane de a trece Carpații pe văile Oltului și Topologului.</h5>
            <h4><b>Bătălia de pe Valea Jiului</b></h4>
            <h5>Prima bătălie din defileul Jiului s-a desfășurat între 23 - 29 octombrie 1916. Obiectivul forțelor germano - austro-ungare era de a trece munții Carpați prin trecătorile Surduc (Lainici) și Vâlcan.</h5>
            <h4><b>Operația de apărare a Bucureștiului</b></h4>
            <h5>„Bătălia pentru București”, a avut ca obiectiv stoparea ofensivei forțelor Puterilor Centrale în Muntenia și prevenirea capturării capitalei țării, orașul București, de către acestea. Bătălia, desfășurată între 30 noiembrie și 3 decembrie 1916, a avut un caracter defensiv, la acțiunile militare participând alături de Armata României și un contingent din armata imperială rusă. Forțele atacatoare au fost preponderent germane, reprezentate de două grupări armate care atacau concentric, din Oltenia și din sudul Dunării, dar și de forțe bulgare și turce.</h5>



   			
