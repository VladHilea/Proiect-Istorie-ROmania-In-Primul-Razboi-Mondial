				<h4><b>Bătălia de la Mărăști</b></h4>
            <h5>Bătălia de la Mărăști a fost prima din marile bătălii desfășurate pe teritoriul României în timpul campaniei anului 1917. S-a desfășurat între 22 iulie și 1 august 1917 fiind concepută ca o operație ofensivă pe o direcție secundară a armatelor române și ruse, în paralel cu operatia ofensivă principală de la Nămoloasa, la care s-a renunțat între timp. </h5>
            <h5>Bătălia de la Mărăști s-a încheiat cu victoria forțelor române care au împins frontul înapoi pe o lățime de 30 de km și o adâncime de 20 de kilometri, eliberând peste 500 de kilometri pătrați și 30 de localități. Pierderile proprii au fost de 110 ofițeri și 4.782 de trupă (morți, răniți și dispăruți), iar cele provocate inamicului au constat în 32 ofițeri și 2.761 trupă prizonieri și o importantă cantitate de material de război.</h5>
        <h4><b>A treia Bătălie de la Oituz</b></h4>
            <h5>A treia Bătălie de la Oituz a avut loc între 26 iulie/8 august - 9/22 august 1917, concomitent cu Bătălia de la Mărășești. Acțiunile militare s-au desfășurat în două etape. Prima etapă a fost între 26 iulie/8 august - 5/18 august iar cea de-a doua etapă între 6/19—9/22 august, ambele etape reprezentând lupte duse pentru respingerea a două încercări ale forțelor Grupului Gerok, de a pătrunde în valea Trotușului. </h5>

            <h2>Pe 27 martie 1918, Sfatul Țării din Basarabia a proclamat unirea cu România cu o majoritate a voturilor.Pe 14 octombrie, reprezentanții populației din Bucovina a votat pentru unirea cu Țara (unirea realizându-se pe 15 noiembrie), iar pe 1 decembrie același an, și reprezentanții românilor și sașilor din Transilvania a adoptat proclamația de la Alba Iulia de unire cu Regatul Român. </h2>

